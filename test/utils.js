export function addtest(input1 , input2) {
    return input1 + input2
}

export function subtest(input3 , input4) {
    return input3 - input4
}

export function multtest(input5 , input6) {
    return input5 * input6
}

export function divtest(input7 , input8)  {
    return input7 / input8
}       

